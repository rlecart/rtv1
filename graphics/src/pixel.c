#include <graphics.h>

void	pixel_put(char *data, int x, int y, t_color pix)
{
	int		i;

	i = 0;
	if (x < WIN_W && y < WIN_H && x >= 0 && y >= 0)
	{
		i = y * WIN_W + x;
		((t_color*)data)[i] = pix;
	}
}
