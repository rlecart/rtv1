#include <graphics.h>

t_v3f	get_vector(int x, int y, float dir[3])
{
	t_v3f	dis;

	dir[0] = (dir[0] > 360) ? -360 : dir[0];
	dir[1] = (dir[1] > 360) ? -360 : dir[1];
	dir[2] = (dir[2] > 360) ? -360 : dir[2];
	dir[0] = (dir[0] < -360) ? 360 : dir[0];
	dir[1] = (dir[1] < -360) ? 360 : dir[1];
	dir[2] = (dir[2] < -360) ? 360 : dir[2];
	dis.x = 250 + dir[0];
	dis.y = ((WIN_W / 2) - x) + dir[1];
	dis.z = ((WIN_H / 2) - y) + dir[2];
	return (dis);
}

void	add_3f(float (*a)[3], float (*b)[3], float (*c)[3])
{
	(*a)[0] = (*b)[0] + (*c)[0];
	(*a)[1] = (*b)[1] + (*c)[1];
	(*a)[2] = (*b)[2] + (*c)[2];
}

void	mul_3f(float (*a)[3], float (*b)[3], float (*c)[3])
{
	(*a)[0] = (*b)[0] * (*c)[0];
	(*a)[1] = (*b)[1] * (*c)[1];
	(*a)[2] = (*b)[2] * (*c)[2];
}
